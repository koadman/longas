package Phylosift::Settings;
use warnings;
use strict;
use Carp;

our $VERSION = "v1.0.1";

=head1 Name

Phylosift::Settings - Sets up the parameters from RC files, command line or default values.

=head1 VERSION

Version 0.01

=cut

=head2 set_default

	Sets default value to a variable if it hasn't been already been initialized

=cut

sub set_default {
	my %args  = @_;
	my $param = $args{parameter};
	my $value = $args{value};
	my $force = $args{force} || 0;
	if ( !defined( ${$param} ) || $force ) {
		${$param} = $value;
	}
}

